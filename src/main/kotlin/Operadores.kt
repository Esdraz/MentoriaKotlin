fun main() {
    //Operadores aritmeticos
    val n1 = 10
    val n2 = 5

    val soma = n1 + n2
    val subtracao = n1 - n2
    val multiplicacao = n1 * n2
    val divisao = n1 / n2
    val resto = n1 % n2

    //Operadores relacionais
    val igual = n1 == n2
    val diferente = n1 != n2
    val maiorQue = n1 > n2
    val menorQue = n1 < n2
    val maiorOuIgual = n1 >= n2
    val menorOuIgual = n1 <= n2

    //Operadores Logicos
    val resultadoE = true && true
    val resultadoOU = true || false
    val resultadoNegacao = !true


    println("Operadores Aritmeticos =====|")
    println(soma)
    println(subtracao)
    println(multiplicacao)
    println(divisao)
    println(resto)
    println("Operadores Relacionais =====|")
    println(igual)
    println(diferente)
    println(maiorQue)
    println(menorQue)
    println(maiorOuIgual)
    println(menorOuIgual)
    println("Operadores Logicos =====|")
    println(resultadoE)
    println(resultadoOU)
    println(resultadoNegacao)
}